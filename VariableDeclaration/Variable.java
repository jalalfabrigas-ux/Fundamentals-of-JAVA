package VariableDeclaration;

public class Variable {
    public static void main(String[] args){

        String name = "JALAL";
        char middleInitial = 'F';
        int age = 19;
        double favNum = 4.14;

        System.out.print("\nNAME: " + name);
        System.out.print("\nInitial: " + middleInitial);
        System.out.print("\nAge: " + age);
        System.out.print("\nFAVORITE NUMBER: " + favNum);
    }
    
}
