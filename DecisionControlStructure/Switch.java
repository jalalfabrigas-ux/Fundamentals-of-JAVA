package DecisionControlStructure;
public class Switch{
    public static void main(String args[]){

        int example=2;

        switch(example){
            case 1:
                System.out.print("CASE 1");
                break;
            case 2:
                System.out.print("CASE 2");
                break;
            case 3:
                System.out.print("CASE 3");
                break;
            default:
                System.out.print("OPERATION ERROR!");
                break;
            }

    }
}